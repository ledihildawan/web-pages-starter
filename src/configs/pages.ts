export function getSystemPageSlug(pageId: string, locale: string): string {
  const entry = SYSTEM_PAGE_SLUGS[pageId as SystemPageId];
  if (!entry) return pageId;
  return entry[locale] ?? entry['en-US'] ?? pageId;
}

export function getRootPageSlug(locale: string): string {
  return getSystemPageSlug(ROOT_PAGE, locale);
}

export function getErrorPageSlugs(locale: string): string[] {
  return SYSTEM_PAGE_IDS.filter((id) => id !== ROOT_PAGE).map((id) =>
    getSystemPageSlug(id, locale),
  );
}

export function isSystemPageId(pageId: string): boolean {
  return pageId in SYSTEM_PAGE_SLUGS;
}

export function isSystemPageSlug(slug: string, locale: string): boolean {
  return SYSTEM_PAGE_IDS.some((id) => getSystemPageSlug(id, locale) === slug);
}
